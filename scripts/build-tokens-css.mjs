#!/usr/bin/env node
// Generates app/tokens.generated.css from the design-token JSON single source of
// truth, so CSS variables can NEVER drift from colors.json/typography.json/
// spacing.json (FRONTEND §4.1). Re-run whenever a token changes; the output is a
// build artifact, not hand-edited. Zero dependencies.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const read = (p) => JSON.parse(readFileSync(resolve(root, p), "utf8"));

const colors = read("design/tokens/colors.json");
const type = read("design/tokens/typography.json");
const spacing = read("design/tokens/spacing.json");

const block = (sel, vars) =>
  `${sel} {\n${Object.entries(vars)
    .map(([k, v]) => `  --${k}: ${v};`)
    .join("\n")}\n}\n`;

const typeVars = Object.fromEntries(
  Object.entries(type.scale).flatMap(([token, def]) => [
    [`${token}-size`, def.size],
    [`${token}-lh`, def.lineHeight],
  ]),
);

const out =
  `/* AUTO-GENERATED by scripts/build-tokens-css.mjs — DO NOT EDIT.\n` +
  `   Source of truth: design/tokens/*.json (FRONTEND_DESIGN_PLAN.md §2.1-2.3, §4.1). */\n\n` +
  block(":root", { ...colors.light, ...typeVars }) +
  `\n` +
  block(':root[data-theme="dark"], :root.dark', colors.dark) +
  `\n@media (prefers-color-scheme: dark) {\n` +
  block("  :root:not([data-theme])", colors.dark) +
  `}\n` +
  `\n:root {\n  --content-article: ${spacing.contentWidth.article};\n  --content-index: ${spacing.contentWidth.index};\n  --cover-aspect: ${spacing.coverAspectRatio};\n}\n`;

writeFileSync(resolve(root, "app/tokens.generated.css"), out);
console.log("✓ app/tokens.generated.css written from design/tokens/*.json");
